class DictType: 
    DictNumber = {1:'John', 2:'Wick', 
                  3:'Barry', 4:'Allen'} 
      
    print(type(DictNumber)) 
    
class ListType: 
    ListNumber = [1, 2, 3, 4, 5] 
      
    print(type(ListNumber)) 
    
class TupleType: 
    TupleNumber = ('Geeks', 'for', 'geeks') 
      
    print(type(TupleNumber)) 
      
d = DictType() 
